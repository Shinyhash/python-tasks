def abc(menu):
    print("Меню детского кафе:")
    for a, b in menu.items():
        print(f"Наименование: {a}")
        print(f"Вес: {b['w']}")
        print(f"Стоимость: {b['p']} руб.")
        print()

menu = {
    'Гамбургер': {
        'w': '100 г',
        'p': 150
    },
    'Картошка фри': {
        'w': '150 г',
        'p': 100
    },
    'Сок апельсиновый': {
        'w': '200 мл',
        'p': 50
    },
    'Мороженое': {
        'w': '100 г',
        'p': 75
    }
}

abc(menu)
