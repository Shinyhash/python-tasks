def abc(s):
    print("Студенческая ведомость:")
    for st, d in s.items():
        print(f"Ф.И.О.: {st}")
        print(f"Оценки: {', '.join(map(str, d['g']))}")
        print(f"Средний балл: {d['a']}")
        print()

s = {
    'Титова Елизавета Артемьевна': {'g': [4, 5, 3],'a': 4.0},
    'Попова Полина Дмитриевна': {'g': [5, 4, 5],'a': 4.7},
    'Токарев Андрей Ильич': {'g': [3, 3, 4],'a': 3.3}
}

abc(s)
