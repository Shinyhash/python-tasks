def abc(s):
    print("Студенческая ведомость:")
    for s, d in s.items():
        print(f"Ф.И.О.: {s}")
        print(f"Оценки: {', '.join(map(str, d['g']))}")
        print(f"Средний балл: {d['a']}")
        print()

s = {
    'Титова Елизавета Артемьевна': {
        'g': [4, 5, 3],
        'a': 4.0
    },
    'Попова Полина Дмитриевна': {
        'g': [5, 5, 5],
        'a': 5
    },
    'Токарев Андрей Ильич': {
        'g': [4, 4, 4],
        'a': 4
    }
}

abc(s)
