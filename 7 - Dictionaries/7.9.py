def abc(f):
    print("Сведения о периодических изданиях:")
    for p, d in f.items():
        print(f"Наименование издания: {p}")
        print(f"Тираж: {d['c']}")
        print(f"Годовая стоимость: {d['a']} руб.")
        print()

p = {
    'Газета "The Times"': {
        'c': 50000,
        'a': 2000
    },
    'Журнал "Forbes"': {
        'c': 12000,
        'a': 2500
    }
}

abc(p)
